package librairie;

public class Roman extends Livre {
//////////////////////////////////////////////////////////////////////
//Variables 
//////////////////////////////////////////////////////////////////////

private int prixLitteraire;

//////////////////////////////////////////////////////////////////////
//Getters & setters 
//////////////////////////////////////////////////////////////////////

public int getPrixLitteraire() {
	return prixLitteraire;
}

public void setPrixLitteraire(int prixLitteraire) {
	this.prixLitteraire = prixLitteraire;
}

//////////////////////////////////////////////////////////////////////
//toString
//////////////////////////////////////////////////////////////////////

@Override
public String toString() {
	return "Roman [distance=" + distance + ", titre=" + titre + ", auteur=" + auteur + ", langue=" + langue + "]";
}

//////////////////////////////////////////////////////////////////////
//Constructor
//////////////////////////////////////////////////////////////////////

public Roman(int distance, String titre, String auteur, String langue, int distance2, String titre2, String auteur2,
		String langue2) {
	super(distance, titre, auteur, langue);
	distance = distance2;
	titre = titre2;
	auteur = auteur2;
	langue = langue2;
}
}
